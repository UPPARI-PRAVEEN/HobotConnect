export default function HiddenPathGame() {
  const [levelIndex, setLevelIndex] = useState(0);
  const level = LEVELS[levelIndex];
  const [player, setPlayer] = useState(level.start);
  const [collectedKeys, setCollectedKeys] = useState([]);
  const [trail, setTrail] = useState([level.start]);
  const [showPath, setShowPath] = useState(false);
  const [phase, setPhase] = useState(0); // 0 = before key1, 1 = before key2, ...

  useEffect(() => {
    setPlayer(level.start);
    setCollectedKeys([]);
    setTrail([level.start]);
    setShowPath(false);
  }, [levelIndex]);

  const isSame = (a, b) => a[0] === b[0] && a[1] === b[1];

  const isValidMove = (r, c) =>
    level.paths[phase].some(([x, y]) => x === r && y === c);

  const handleCellClick = (r, c) => {
    const [pr, pc] = player;

    // STRICT adjacent rule
    if (Math.abs(pr - r) + Math.abs(pc - c) !== 1) return;

    // ONE mistake = reset level
    if (!isValidMove(r, c)) {
      resetLevel();
      return;
    }

    const next = [r, c];
    setPlayer(next);
    setTrail((t) => [...t, next]);

    // collect keys
    level.keys.forEach((k, index) => {
      if (isSame(k, next) && phase === index) {
        setCollectedKeys((prev) => [...prev, k]);
        setPhase((p) => p + 1); // 🔥 unlock next path
      }
    });

    // door logic – NEED ALL KEYS
    if (isSame(next, level.door)) {
      if (phase === level.keys.length) {
        nextLevel();
      } else {
        resetLevel();
      }
    }
  };

  const resetLevel = () => {
    setPlayer(level.start);
    setCollectedKeys([]);
    setTrail([level.start]);
    setShowPath(false);
    setPhase(0);
  };

  const nextLevel = () => {
    if (levelIndex === LEVELS.length - 1) {
      alert("🏆 HARD MODE CLEARED!");
      setLevelIndex(0);
    } else {
      setLevelIndex((i) => i + 1);
    }
  };

  return (
    <div style={{ textAlign: "center" }}>
      <h2>Hard Mode – Level {level.id}</h2>

      {/* SHOW PATH BUTTON */}
      <button
        onClick={() => setShowPath((p) => !p)}
        style={{
          marginBottom: 12,
          padding: "6px 16px",
          borderRadius: 8,
          border: "none",
          cursor: "pointer",
          fontWeight: "bold"
        }}
      >
        {showPath ? "Hide Path" : "Show Path"}
      </button>

      <div
        style={{
          display: "grid",
          gridTemplateColumns: `repeat(${level.size}, 60px)`,
          gap: 8,
          justifyContent: "center"
        }}
      >
        {Array.from({ length: level.size * level.size }).map((_, i) => {
          const r = Math.floor(i / level.size);
          const c = i % level.size;
          const isPlayer = isSame(player, [r, c]);
          const isTrail = trail.some((p) => isSame(p, [r, c]));
          const isPath =
            showPath && level.paths[phase].some((p) => isSame(p, [r, c]));
          const isKey = level.keys.some((k) => isSame(k, [r, c]));
          const isCollected = collectedKeys.some((k) => isSame(k, [r, c]));

          let content = "";
          if (isPlayer) content = "🙂";
          else if (isKey && !isCollected) content = "🔑";
          else if (isSame([r, c], level.door)) content = "🚪";

          return (
            <div
              key={i}
              onClick={() => handleCellClick(r, c)}
              style={{
                width: 60,
                height: 60,
                borderRadius: 12,
                background: isPlayer
                  ? "#000"
                  : isPath
                  ? "#0a7" // SHOW PATH (GREEN)
                  : isTrail
                  ? "#333"
                  : "#eee",
                color: isPlayer || isTrail ? "#fff" : "#000",
                fontSize: 24,
                display: "flex",
                alignItems: "center",
                justifyContent: "center",
                cursor: "pointer",
                userSelect: "none"
              }}
            >
              {content}
            </div>
          );
        })}
      </div>

      <p style={{ marginTop: 12, fontSize: 14 }}>
        🔑 {collectedKeys.length}/{level.keys.length} keys collected · One
        mistake resets
      </p>
    </div>
  );
}
